a = 10
b = 3

print(a + b)    # 13   — addition
print(a - b)    # 7    — subtraction
print(a * b)    # 30   — multiplication
print(a / b)    # 3.3333333333333335 — division (always float)
print(a // b)   # 3    — floor division (whole part only)
print(a % b)    # 1    — modulus (remainder: 10 = 3×3 + 1)
print(a ** b)   # 1000 — exponent (10 to the power 3)